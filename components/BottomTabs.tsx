"use client";

import { useSession, signOut } from "next-auth/react";
import { usePathname } from "next/navigation";
import Link from "next/link";

interface TabItem {
  href: string;
  label: string;
  icon: React.ReactNode;
}

const studentTabs: TabItem[] = [
  { href: "/dashboard", label: "Home", icon: <HomeTabIcon /> },
  { href: "/flashcards", label: "Cards", icon: <CardsTabIcon /> },
  { href: "/practice", label: "Practice", icon: <PracticeTabIcon /> },
  { href: "/quiz", label: "Quiz", icon: <QuizTabIcon /> },
  { href: "/leaderboard", label: "Ranks", icon: <TrophyTabIcon /> },
];

const adminTabs: TabItem[] = [
  { href: "/dashboard", label: "Home", icon: <HomeTabIcon /> },
  { href: "/admin/questions", label: "Questions", icon: <QuestionTabIcon /> },
  { href: "/admin/import", label: "Import", icon: <ImportTabIcon /> },
  { href: "/admin/students", label: "Students", icon: <StudentsTabIcon /> },
  { href: "/admin/settings", label: "Settings", icon: <SettingsTabIcon /> },
];

export default function BottomTabs() {
  const { data: session } = useSession();
  const pathname = usePathname();

  const role = session?.user?.role;
  const tabs = role === "admin" ? adminTabs : studentTabs;

  return (
    <nav
      className="md:hidden fixed bottom-0 left-0 right-0 border-t flex z-50"
      style={{
        backgroundColor: "var(--surface)",
        borderColor: "var(--border-light)",
      }}
    >
      {tabs.map((tab) => {
        const isActive =
          pathname === tab.href ||
          (tab.href !== "/dashboard" && pathname.startsWith(tab.href));
        return (
          <Link
            key={tab.href}
            href={tab.href}
            className="flex-1 flex flex-col items-center gap-0.5 py-2 text-xs transition-colors"
            style={{
              color: isActive ? "var(--interactive)" : "var(--text-tertiary)",
            }}
          >
            {tab.icon}
            <span className="font-medium">{tab.label}</span>
          </Link>
        );
      })}
      <button
        onClick={() => signOut({ callbackUrl: "/login" })}
        className="flex-1 flex flex-col items-center gap-0.5 py-2 text-xs transition-colors"
        style={{ color: "var(--text-tertiary)" }}
      >
        <LogoutTabIcon />
        <span className="font-medium">Log out</span>
      </button>
    </nav>
  );
}

